chosen_number = int(input('Choose A Number From 1 - 7: ')) 
if chosen_number == 1 :  
    print("Sunday") 
elif chosen_number ==2 :  
    print("Monday") 
elif chosen_number ==3 :  
    print("Tuesday") 
elif chosen_number ==4 :  
    print("Wednesday") 
elif chosen_number ==5 :  
    print("Thursday") 
elif chosen_number ==6 :  
    print("Friday") 
elif chosen_number ==7 :  
    print("Saturday") 
else : 
    print("Value Must Be Between 1 and 7") 